#!/bin/bash

echo -e "\n-----------------------------------------------------------------"
echo -e 'Cleaning...'
echo -e '-----------------------------------------------------------------\n'
shopt -s extglob
for file in *; do
	if [[ $file != *.java && $file != runJava.sh ]]; then
	rm -v "$file"
fi
done
echo -e '\n-----------------------------------------------------------------'
echo 'Cleaning complete.'
echo -e '-----------------------------------------------------------------\n'

echo -e '\n-----------------------------------------------------------------'
echo 'Compiling and creating .class files...'
echo -e '-----------------------------------------------------------------\n'
javac -g -Xdiags:verbose -Werror *.java
echo -e '\n-----------------------------------------------------------------'
echo 'Running...'
echo -e '-----------------------------------------------------------------\n'
java -cp ../../ HW.HW7.driver2
echo -e '\n-----------------------------------------------------------------'
echo 'Program has ended.'
echo -e '-----------------------------------------------------------------\n'
